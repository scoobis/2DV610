package MathGame;

public class MathQuestions {
	
	public int simpleAddition(int num1, int num2) { return num1 + num2; }
	
	public int simpleSubtraction(int num1, int num2) { return num1 - num2; }
	
	public int simpleMultiplication(int num1, int num2) { return num1 * num2; }
	
	public int simpleDivision(int num1, int num2) { return num1 / num2; }
	
	public int squareRoot (int num) { return (int) Math.sqrt(9); }
}
